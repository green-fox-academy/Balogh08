public class Demo {
    private String firstName;
    private String lastName;
    private int age;
    private int favNumber;
    private String city;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }

    public int getFavNumber() {
        return favNumber;
    }

    public String getCity() {
        return city;
    }

    public Demo(String firstName, String lastName, int age, int favNumber, String city) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.favNumber = favNumber;
        this.city = city;
    }
}













